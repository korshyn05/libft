#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new;
	int		i;

	if (!(new = malloc(sizeof(char) * (ft_strlen(src) + 1))))
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
