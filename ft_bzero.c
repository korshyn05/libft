#include "libft.h"

void	ft_bzero(void *p, size_t n)
{
	char	*ptr;

	if (!n)
		return ;
	ptr = (char *)p;
	while (n != 0)
	{
		*ptr = '\0';
		ptr++;
		n--;
	}
}
